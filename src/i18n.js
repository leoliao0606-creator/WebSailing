const STRINGS = { zh, en, ja };

let cur = 'zh';

export function detectLang() {
  const n = (navigator.language || 'zh').toLowerCase();
  if (n.startsWith('zh')) return 'zh';
  if (n.startsWith('ja')) return 'ja';
  return 'en';
}

export function setLang(code) {
  cur = STRINGS[code] ? code : 'zh';
  document.documentElement.lang = cur === 'zh' ? 'zh-CN' : cur;
  document.title = t('title.full');
}

export function getLang() { return cur; }

export function t(key, vars) {
  let s = STRINGS[cur][key] ?? STRINGS.zh[key] ?? key;
  if (vars) for (const k in vars) s = s.replaceAll(`{${k}}`, vars[k]);
  return s;
}
